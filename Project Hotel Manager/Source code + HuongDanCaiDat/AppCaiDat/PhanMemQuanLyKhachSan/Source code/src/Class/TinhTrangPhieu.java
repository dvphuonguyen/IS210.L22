package Class;

public class TinhTrangPhieu {
	private int maTT;
	private String tenTT;
	public TinhTrangPhieu(int maTT, String tenTT) {
		super();
		this.maTT = maTT;
		this.tenTT = tenTT;
	}
	public int getMaTT() {
		return maTT;
	}
	public void setMaTT(int maTT) {
		this.maTT = maTT;
	}
	public String getTenTT() {
		return tenTT;
	}
	public void setTenTT(String tenTT) {
		this.tenTT = tenTT;
	}
	
}
